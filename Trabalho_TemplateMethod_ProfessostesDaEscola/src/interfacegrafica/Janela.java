    private void imprimirNaGrid(ArrayList<Professor> dados){
        try{
            DefaultTableModel model =  (DefaultTableModel) jTableGridProfessores.getModel();
            model.setNumRows(0);
            int pos = 0;
            
                while(pos < dados.size()){
                String[] linha = new String[4];
                Professor obj = dados.get(pos);
                linha[0]= obj.getMatricula();
                linha[1]= obj.getNome();
                linha[2]= obj.getEscola();
                linha[3]= obj.getTitulacao();
                model.addRow(linha);
                pos++;
            }
        }catch(Exception erro){
            JOptionPane.showMessageDialog(rootPane, erro);
        }
    }
                
    
